
""" Contoh OOP yang kurang efektif """
class User:
    pass

# ilyas, otong
# mempunyai atribut name, email, role
# class adalah sebuah cetakan
# object adalah sebuah hasil dari cetakannya

ilyas = User()
otong = User()

ilyas.name = "ilyas" # name adalah attribute
otong.name = "otong" # otong adalah sebuah object dari class User yg dimana nilai attribute namenya otong.

print(ilyas.name)
print(otong.name)

